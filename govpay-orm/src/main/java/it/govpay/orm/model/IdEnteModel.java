package it.govpay.orm.model;

import it.govpay.orm.IdEnte;

import org.openspcoop2.generic_project.beans.AbstractModel;
import org.openspcoop2.generic_project.beans.IField;
import org.openspcoop2.generic_project.beans.Field;
import org.openspcoop2.generic_project.beans.ComplexField;


/**     
 * Model IdEnte 
 *
 * @author Giovanni Bussu (bussu@link.it)
 * @author Lorenzo Nardi (nardi@link.it)
 * @author $Author$
 * @version $Rev$, $Date$
 */
public class IdEnteModel extends AbstractModel<IdEnte> {

	public IdEnteModel(){
	
		super();
	
		this.COD_ENTE = new Field("codEnte",java.lang.String.class,"id-ente",IdEnte.class);
	
	}
	
	public IdEnteModel(IField father){
	
		super(father);
	
		this.COD_ENTE = new ComplexField(father,"codEnte",java.lang.String.class,"id-ente",IdEnte.class);
	
	}
	
	

	public IField COD_ENTE = null;
	 

	@Override
	public Class<IdEnte> getModeledClass(){
		return IdEnte.class;
	}
	
	@Override
	public String toString(){
		if(this.getModeledClass()!=null){
			return this.getModeledClass().getName();
		}else{
			return "N.D.";
		}
	}

}