	@Override
	public List<IdEr> findAllIds(JDBCServiceManagerProperties jdbcProperties, Logger log, Connection connection, ISQLQueryObject sqlQueryObject, JDBCPaginatedExpression expression, org.openspcoop2.generic_project.beans.IDMappingBehaviour idMappingResolutionBehaviour) throws NotImplementedException, ServiceException,Exception {

        // default behaviour (id-mapping)
        if(idMappingResolutionBehaviour==null){
                idMappingResolutionBehaviour = org.openspcoop2.generic_project.beans.IDMappingBehaviour.valueOf("USE_TABLE_ID");
        }

		List<IdEr> list = new ArrayList<IdEr>();

		try{
			List<IField> fields = new ArrayList<IField>();

			fields.add(ER.model().COD_MSG_ESITO);

			List<Map<String, Object>> returnMap = this.select(jdbcProperties, log, connection, sqlQueryObject, expression, fields.toArray(new IField[1]));

			for(Map<String, Object> map: returnMap) {
				list.add(this.convertToId(jdbcProperties, log, connection, sqlQueryObject, (ER)this.getERFetch().fetch(jdbcProperties.getDatabase(), ER.model(), map)));
			}
		} catch(NotFoundException e) {}

        return list;
		
	}
	
	@Override
	public List<ER> findAll(JDBCServiceManagerProperties jdbcProperties, Logger log, Connection connection, ISQLQueryObject sqlQueryObject, JDBCPaginatedExpression expression, org.openspcoop2.generic_project.beans.IDMappingBehaviour idMappingResolutionBehaviour) throws NotImplementedException, ServiceException,Exception {

        // default behaviour (id-mapping)
        if(idMappingResolutionBehaviour==null){
                idMappingResolutionBehaviour = org.openspcoop2.generic_project.beans.IDMappingBehaviour.valueOf("USE_TABLE_ID");
        }

        List<ER> list = new ArrayList<ER>();
        
		try{
			List<IField> fields = new ArrayList<IField>();

			fields.add(new CustomField("id", Long.class, "id", this.getERFieldConverter().toTable(ER.model())));
			fields.add(ER.model().COD_MSG_ESITO);
			fields.add(ER.model().DATA_ORA_MSG_ESITO);
			fields.add(ER.model().IMPORTO_TOTALE_REVOCATO);
			fields.add(ER.model().STATO);
			fields.add(ER.model().DESCRIZIONE_STATO);
			fields.add(ER.model().DATA_ORA_CREAZIONE);
			fields.add(new CustomField("id_rr", Long.class, "id_rr", this.getERFieldConverter().toTable(ER.model())));
			fields.add(new CustomField("id_tracciato_xml", Long.class, "id_tracciato_xml", this.getERFieldConverter().toTable(ER.model())));
			
			List<Map<String, Object>> returnMap = this.select(jdbcProperties, log, connection, sqlQueryObject, expression, fields.toArray(new IField[1]));

			for(Map<String, Object> map: returnMap) {
				Long id_rr = (Long) map.remove("id_rr");
				Long id_tracciato_xml = (Long) map.remove("id_tracciato_xml");

				ER er = (ER)this.getERFetch().fetch(jdbcProperties.getDatabase(), ER.model(), map);
				
				it.govpay.orm.IdRr id_er_rr = null;
				if(idMappingResolutionBehaviour==null || org.openspcoop2.generic_project.beans.IDMappingBehaviour.ENABLED.equals(idMappingResolutionBehaviour)){
					id_er_rr = ((JDBCRRServiceSearch)(this.getServiceManager().getRRServiceSearch())).findId(id_rr, false);
				}else{
					id_er_rr = new it.govpay.orm.IdRr();
				}
				id_er_rr.setId(id_rr);
				er.setIdRR(id_er_rr);
				
				it.govpay.orm.IdTracciato id_er_tracciatoXML = null;
				if(idMappingResolutionBehaviour==null || org.openspcoop2.generic_project.beans.IDMappingBehaviour.ENABLED.equals(idMappingResolutionBehaviour)){
					id_er_tracciatoXML = ((JDBCTracciatoXMLServiceSearch)(this.getServiceManager().getTracciatoXMLServiceSearch())).findId(id_tracciato_xml, false);
				}else{
					id_er_tracciatoXML = new it.govpay.orm.IdTracciato();
				}
				id_er_tracciatoXML.setId(id_tracciato_xml);
				er.setIdTracciatoXML(id_er_tracciatoXML);


				list.add(er);
			}
		} catch(NotFoundException e) {}

        return list;      
		
	}
	
