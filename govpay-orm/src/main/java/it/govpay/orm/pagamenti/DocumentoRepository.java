package it.govpay.orm.pagamenti;

import it.govpay.orm.BaseEntity;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.Lob;
import javax.persistence.Table;

@Entity
@Table(name = "documenti_repository")
public class DocumentoRepository extends BaseEntity {
	private static final long serialVersionUID = 1L;

	private Integer dimensione;
	private byte[] documento;
	private String nomeFile;

	/*** Auto Generated Identity Property ***/
	private Long id;

	public DocumentoRepository() {
	}

	public Integer getDimensione() {
		return this.dimensione;
	}

	public void setDimensione(Integer dimensione) {
		this.dimensione = dimensione;
	}

	@Lob()
	public byte[] getDocumento() {
		return this.documento;
	}

	public void setDocumento(byte[] documento) {
		this.documento = documento;
	}

	@Column(name = "NOME_FILE")
	public String getNomeFile() {
		return this.nomeFile;
	}

	public void setNomeFile(String nomeFile) {
		this.nomeFile = nomeFile;
	}

	@Id
	@GeneratedValue(strategy = GenerationType.IDENTITY)
	public Long getId() {
		return this.id;
	}

	public void setId(Long id) {
		this.id = id;
	}

}